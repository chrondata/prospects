<h2 class="tap-content-section-title">About the data</h2>
<p>The sources of the data in this project include 2013 state boundaries and 2012 county boundaries as provided by the National Atlas of the United States, U.S. Geological Survey. Census-tract boundaries are as of 2013, from the National Historical Geographic Information System at the University of Minnesota. City and town locations are based on 2010 U.S. Census files. Median household income and adult educational attainment are from data collected from 2007 through 2011 in the U.S. Census Bureau's American Community Survey.</p>
<p>Population data were pulled from the 2010 U.S. Census. Because this project is being published in 2014, the ages, set as of April 1, have been adjusted by four years. Each dot, which represents 10 people, is randomly distributed within each census tract to show population density.</p>
<p>According to the U.S. Census, people who identify themselves as being of Hispanic or Latino origin may be of any race. For example, a person can identify himself as both black and Hispanic. For this project, we used Census data that separate Hispanic and Latino into their own category and exclude them from the other racial and ethnic categories. Those who identify as belonging to more than one non-Hispanic racial group (for example, white and African-American) are represented exclusively in the "two or more races" category.</p>
<p>In determining area categorizations, (i.e., "Low-income and highly educated"), we ranked every county across five measures: average age, average household income, percentage of adults 25 or older who have a bachelor's degree, degree of ethnic diversity, and total population. For each area, we selected the two categories for which the area was the most extreme. That is, if it was in the 1st percentile for income, the 10th percentile for education, and the 95th percentile for diversity, we highlighted the income and diversity categories, as those are the most extreme.</p>
<p>We broke each category into five segments. If an area's category ranks in the top or bottom 17 percentiles, it falls into one of the two extreme segments, such as "low-income" or "high-income." If the ranking is in the 38th through 62nd percentile, that would put the category into the middle, or average, segment, (i.e., "of average income"). And if the percentile rank fell into either of the two other ranges, then it would be labeled as "somewhat," as in "somewhat low-income" or "somewhat high-income."</p>
